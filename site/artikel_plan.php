              <h3>6. HUBUNGI PERUSAHAAN KARTU KREDIT ANDA</h3>
              <figure class="figure-light"><img class="img-centered" src="images/tujuan/kredit.jpg" alt="" width="570" height="280"/>
                <figcaption>
                  <p>The Best Tours</p>
                </figcaption>
              </figure>
              <p>Bayangkan jika Anda akhirnya bisa melakukan perjalanan liburan ke Paris yang selama ini Anda impikan. Anda dapat menikmati pemandangan kota paris, suara, dan lalu lalang orang disana. 
                Kemudian datang waktunya untuk membayar tagihan Anda. Kemudian Anda menyerahkan kartu kredit Anda dan kemudian pelayan akan memindai kartu kredit Anda tersebut dan scan... dan scan lagi. Dia lantas menggelengkan kepalanya yang artinya kartu kredit Anda di tolak.</p>
                <p>Sementara itu, Anda tidak memiliki cara lain untuk membayarnya karena Anda memang tidak membawa uang cash. Ini adalah apa yang bisa saja terjadi (mungkin) jika Anda tidak memperingatkan perusahaan kartu kredit Anda tentang perjalanan yang Anda lakukan.
                  Perusahaan kartu kredit akan merasa curiga ketika mereka melihat Anda melakukan pembelian yang biasanya tidak Anda lakukan, terutama ketika pembelian tersebut di suatu tempat asing dimana Anda tidak tinggal sana. Jadi, selalu ingatkan perusahaan kartu kredit Anda jika Anda akan melakukan perjalanan liburan, terutama keluar negeri agar mereka juga mengetahui jika Anda akan menggunakan kartu kredit tersebut untuk melakukan transaksi disana.
                  </p>
