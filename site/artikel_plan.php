              <p>Padahal menurut undang - undang tenaga kerja yang berlaku di Indonesia, setiap tahunnya para pekerja mendapatkan jatah cuti (libur kerja dengan tetap dibayar) selama 12 hari. Sangat di sayangkan jika mayoritas pekerja tidak bisa mengambil dan memanfaatkannya untuk pergi berlibur.</p>
