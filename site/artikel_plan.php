              <h3>4. TEMUKAN PENERBANGAN YANG TERJANGKAU</h3>
              <figure class="figure-light"><img class="img-centered" src="images/tujuan/tiket.jpg" alt="" width="570" height="280"/>
                <figcaption>
                  <p>The Best Tours</p>
                </figcaption>
              </figure>
              <p>Salah satu kesalahpahaman yang paling umum yang orang pikirkan tentang pergi berlibur adalah harga tiket pesawat terbang (terutama internasional) yang mahal.
                Kenyataannya adalah, tiket pesawat terbang bisa menjadi salah satu biaya yang paling terjangkau dari perjalanan Anda jika mengetahui bagaimana cara memanfaatkan alat yang tepat.
                Dua alat yang paling efektif untuk menemukan penerbangan yang murah dan terjangkau adalah:</p>
                <ul class="list-marked">
                  <li>Reposisi dengan kesalahan tarif</li>
                  <li>Kartu kredit perjalanan</li>
                </ul>
                <p>Jika Anda memesan tiket pesawat terbang dengan menggunakan kartu kredit maka kadang - kadang Anda bisa mendapatkan tiket penerbangan yang murah bahkan gratis.
                  Jika Anda dapat menyimpan ribuan poin reward setiap tahun maka Anda dapat memanfaatkan poin reward tersebut. Dan semakin banyak waktu yang Anda miliki untuk melakukan perencanaan, maka akan semakin banyak poin yang Anda dapat kumpulkan. Ada banyak kartu kredit yang  bahkan dapat  memberikan Anda bonus tiket gratis pesawat.
                  </p>
