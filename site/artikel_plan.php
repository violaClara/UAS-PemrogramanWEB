              <p>Hal pertama yang harus Anda lakukan adalah memutuskan  kemana dan bagaimana Anda akan pergi  pada liburan kali ini. Hal tersebut ditentukan oleh keadaan Anda sendiri, misalnya :</p>
              <ul class="list-marked">
                <li>Apakah Anda lebih terkendala dengan waktu atau dengan uang?</li>
                <li>Apakah Anda akan berlibur sendiri atau dengan teman dan keluarga Anda?</li>
