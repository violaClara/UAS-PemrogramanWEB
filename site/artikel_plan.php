                Salah satu cara yang bisa Anda lakukan untuk menghemat uang untuk perjalanan liburan adalah melalui proses yang disebut "akuntansi mental."
                
                Akuntansi mental adalah teknik psikologis dimana orang memperlakukan uang dengan cara yang berbeda tergantung pada bagaimana mereka mendapatkannya dan berencana untuk menghabiskannya. Contohnya adalah dengan menggunakan rekening sub-tabungan.</p>
