              <p>Traveling atau jalan - jalan adalah seperti permainan Twister, jadi pilihlah yang terbaik namun tetap fleksibel. Ketika Anda akan pergi berlibur maka hal itu sama pentingnya dengan kapan Anda akan pergi, karena waktu yang Anda pilih dapat membuat perbedaan dalam anggaran yang harus Anda persiapkan, jumlahnya bisa ratusan ribu, bahkan hingga jutaan rupiah jika itu menyangkut biaya tiket pesawat.</p>
              <p><i>"Fleksibilitas adalah hal yang paling penting dalam mencari penerbangan murah."</i></p>
              <p>Jika Anda ingin pergi berlibur keluar negeri misalnya pada peak season di bulan Desember menjelang tahun baru, maka pasti harga tiketnya sangatlah mahal. Maka, akan lebih baik jika Anda merencanakan di lain waktu ketika harga tiketnya sudah turun.</p>
